# 优先级排序
GROUP_PRIORITY = {
    "灵活就业": "P0", "35岁被裁": "P0", "全职妈妈": "P0", "鸡娃父母": "P0",
    "农村老人": "P1", "小镇做题家": "P1", "独居中年": "P1", "实体店老板": "P1",
    "独生子女夹心层": "P2", "职场女性": "P2", "应届毕业生": "P2", "退休焦虑": "P2",
    "农民工": "P3", "基民理财": "P3", "大龄未婚": "P3", "AI焦虑": "P3",
    "慢性病": "P3", "残障罕见病": "P3",
}


def match_groups(topics: list[dict]) -> list[dict]:
    """
    给话题列表打上人群标签。
    输入: [{"title": "...", ...}, ...]
    输出: 每项增加 group_tags: ["人群1", "人群2"] 和 group_priority: "P0"
    """
    for topic in topics:
        title = topic.get("title", "")
        matched = []
        for group, keywords in GROUP_TAGS.items():
            for kw in keywords:
                if kw in title:
                    matched.append(group)
                    break
        topic["group_tags"] = matched
        if matched:
            # 取最高优先级
            priorities = [GROUP_PRIORITY.get(g, "P3") for g in matched]
            topic["group_priority"] = min(priorities)
        else:
            topic["group_priority"] = "P3"
    return topics


